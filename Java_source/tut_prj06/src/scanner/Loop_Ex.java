package scanner;

public class Loop_Ex {

	public static void main(String[] args) {
		// Loop - for
		for (int i = 0; i < 10; ++i) {
			System.out.print(i);
		}
		System.out.println("");
		// Loop - for - infinite
		int i = 0;
		while (i < 10) {
			System.out.print(i);
			++i;
		}

	}
}
